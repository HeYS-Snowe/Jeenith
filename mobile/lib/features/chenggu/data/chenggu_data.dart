/// 数字 → 中文（用于重量 label）。
const _cnDigits = ['零', '一', '二', '三', '四', '五', '六', '七', '八', '九'];

/// 钱数 → 中文重量 label，如 39 → "三两九钱"，70 → "七两"。
String chengguWeightLabel(int qian) {
  final liang = qian ~/ 10;
  final qianPart = qian % 10;
  if (qianPart == 0) return '${_cnDigits[liang]}两';
  return '${_cnDigits[liang]}两${_cnDigits[qianPart]}钱';
}

/// 总钱数 → 十进制重量 label（带「两.钱」），如 39 → "3.9 两（三两九钱）"。
String chengguWeightDecimal(int qian) {
  final liang = qian ~/ 10;
  final qianPart = qian % 10;
  return '$liang.$qianPart 两（${chengguWeightLabel(qian)}）';
}
